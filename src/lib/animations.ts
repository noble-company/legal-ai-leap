// Custom variant creator with delay
export const createDelayedVariant = (delay: number, baseVariant = fadeInUpVariants) => ({
  ...baseVariant,
  visible: {
    ...baseVariant.visible,
    transition: {
      ...baseVariant.visible.transition,
      delay,
    },
  },
});

// Variant with custom index-based delay
export const createIndexedVariant = (index: number, baseDelay = 0.1) => ({
  hidden: { opacity: 0, y: 20 },
  visible: {
    opacity: 1,
    y: 0,
    transition: {
      duration: 0.6,
      delay: index * baseDelay,
    },
  },
});
